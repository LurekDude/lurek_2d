    // ── Enhanced raycasting ───────────────────────────────────────────────────

    /// Casts a ray and returns the closest hit using the query pipeline.
    /// Helper to create a query pipeline from the broad phase.
    fn query_pipeline(&self) -> QueryPipeline<'_> {
        self.broad_phase.as_query_pipeline(
            self.narrow_phase.query_dispatcher(),
            &self.rbodies,
            &self.rcolliders,
            QueryFilter::default(),
        )
    }

    /// Casts a ray and returns the closest hit using the query pipeline.
    ///
    /// # Parameters
    /// - `x1` — Ray origin X.
    /// - `y1` — Ray origin Y.
    /// - `dx` — Ray direction X.
    /// - `dy` — Ray direction Y.
    /// - `max_dist` — Maximum ray distance.
    ///
    /// # Returns
    /// The closest `RaycastHit`, or `None`.
    pub fn raycast_closest(
        &self,
        x1: f32,
        y1: f32,
        dx: f32,
        dy: f32,
        max_dist: f32,
    ) -> Option<RaycastHit> {
        let dir_len = (dx * dx + dy * dy).sqrt();
        if dir_len < 1e-6 {
            return None;
        }
        let unit_dir = Vector::new(dx / dir_len, dy / dir_len);
        let ray = Ray::new(Vector::new(x1, y1), unit_dir);

        let qp = self.query_pipeline();
        let (col_handle, toi_result) = qp.cast_ray_and_get_normal(&ray, max_dist, true)?;

        let body_id = self
            .collider_handles
            .iter()
            .position(|&h| h == col_handle)?;
        let pt_x = x1 + unit_dir.x * toi_result.time_of_impact;
        let pt_y = y1 + unit_dir.y * toi_result.time_of_impact;
        Some(RaycastHit {
            body_id,
            point: (pt_x, pt_y),
            normal: (toi_result.normal.x, toi_result.normal.y),
            toi: toi_result.time_of_impact,
        })
    }

    /// Casts a ray and returns all hits along it.
    ///
    /// # Parameters
    /// - `x1` — Ray origin X.
    /// - `y1` — Ray origin Y.
    /// - `dx` — Ray direction X.
    /// - `dy` — Ray direction Y.
    /// - `max_dist` — Maximum ray distance.
    ///
    /// # Returns
    /// A vector of `RaycastHit` results.
    pub fn raycast_all(
        &self,
        x1: f32,
        y1: f32,
        dx: f32,
        dy: f32,
        max_dist: f32,
    ) -> Vec<RaycastHit> {
        let dir_len = (dx * dx + dy * dy).sqrt();
        if dir_len < 1e-6 {
            return Vec::new();
        }
        let unit_dir = Vector::new(dx / dir_len, dy / dir_len);
        let ray = Ray::new(Vector::new(x1, y1), unit_dir);

        let qp = self.query_pipeline();
        let mut hits = Vec::new();
        for (col_handle, _co, ri) in qp.intersect_ray(ray, max_dist, true) {
            if let Some(body_id) = self.body_for_collider(col_handle) {
                let pt_x = x1 + unit_dir.x * ri.time_of_impact;
                let pt_y = y1 + unit_dir.y * ri.time_of_impact;
                hits.push(RaycastHit {
                    body_id,
                    point: (pt_x, pt_y),
                    normal: (ri.normal.x, ri.normal.y),
                    toi: ri.time_of_impact,
                });
            }
        }
        hits
    }

    // ── Spatial queries ───────────────────────────────────────────────────────

    /// Returns body IDs with colliders intersecting the given AABB.
    ///
    /// # Parameters
    /// - `x` — AABB min X.
    /// - `y` — AABB min Y.
    /// - `w` — AABB width.
    /// - `h` — AABB height.
    ///
    /// # Returns
    /// A vector of body indices inside the AABB.
    pub fn query_aabb(&self, x: f32, y: f32, w: f32, h: f32) -> Vec<usize> {
        let aabb = Aabb {
            mins: Vector::new(x, y),
            maxs: Vector::new(x + w, y + h),
        };
        let qp = self.query_pipeline();
        let mut results = Vec::new();
        for (col_handle, _co) in qp.intersect_aabb_conservative(aabb) {
            if let Some(body_id) = self.body_for_collider(col_handle) {
                results.push(body_id);
            }
        }
        results
    }

    /// Returns the first body whose collider contains the given world-space point.
    ///
    /// Uses a point-sized AABB query filter.
    ///
    /// # Parameters
    /// - `x` — World-space X coordinate.
    /// - `y` — World-space Y coordinate.
    ///
    /// # Returns
    /// `Option<usize>` — body index, or `None` if no body at that point.
    pub fn get_body_at_point(&self, x: f32, y: f32) -> Option<usize> {
        let epsilon = 0.01;
        let aabb = Aabb {
            mins: Vector::new(x - epsilon, y - epsilon),
            maxs: Vector::new(x + epsilon, y + epsilon),
        };
        let qp = self.query_pipeline();
        for (col_handle, _co) in qp.intersect_aabb_conservative(aabb) {
            if let Some(body_id) = self.body_for_collider(col_handle) {
                return Some(body_id);
            }
        }
        None
    }

