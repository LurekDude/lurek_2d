// ── Internal event collector ─────────────────────────────────────────────────

/// Collects rapier `CollisionEvent`s during a pipeline step.
/// Uses `Mutex` because rapier2d 0.32 requires `EventHandler: Send + Sync`.
struct LocalEventCollector {
    events: Mutex<Vec<CollisionEvent>>,
}

impl LocalEventCollector {
    fn new() -> Self {
        Self {
            events: Mutex::new(Vec::new()),
        }
    }

    fn drain(&self) -> Vec<CollisionEvent> {
        self.events.lock().expect("event mutex not poisoned").drain(..).collect()
    }
}

impl EventHandler for LocalEventCollector {
    fn handle_collision_event(
        &self,
        _bodies: &RigidBodySet,
        _colliders: &ColliderSet,
        event: CollisionEvent,
        _contact_pair: Option<&ContactPair>,
    ) {
        self.events.lock().expect("event mutex not poisoned").push(event);
    }

    fn handle_contact_force_event(
        &self,
        _dt: f32,
        _bodies: &RigidBodySet,
        _colliders: &ColliderSet,
        _contact_pair: &ContactPair,
        _total_force_magnitude: f32,
    ) {
    }
}

// ── Public types ──────────────────────────────────────────────────────────────

/// Collision event generated by `World::step` when two bodies start overlapping.
///
/// Contains stable integer body IDs for the two bodies involved.
///
/// # Fields
/// - `body_a` — Index of the first body.
/// - `body_b` — Index of the second body.
pub struct BodyContact {
    /// Index of the first body.
    pub body_a: usize,
    /// Index of the second body.
    pub body_b: usize,
}

/// Result of a `World::raycast` query.
///
/// # Fields
/// - `body_id` — Integer body ID that was hit.
/// - `point` — World-space hit point `(x, y)`.
/// - `normal` — World-space surface normal `(nx, ny)`.
/// - `toi` — Distance along the ray to the hit point.
#[derive(Debug, Clone, Copy)]
pub struct RaycastHit {
    /// Integer body ID that was hit.
    pub body_id: usize,
    /// World-space hit point `(x, y)`.
    pub point: (f32, f32),
    /// World-space surface normal `(nx, ny)`.
    pub normal: (f32, f32),
    /// Distance along the ray to the hit point.
    pub toi: f32,
}

/// Contact information between two bodies from the narrow phase.
///
/// # Fields
/// - `body_a` — Index of the first body.
/// - `body_b` — Index of the second body.
/// - `normal_x` — Contact normal X component.
/// - `normal_y` — Contact normal Y component.
/// - `is_touching` — Whether the bodies are currently touching.
#[derive(Debug, Clone)]
pub struct ContactInfo {
    /// Index of the first body.
    pub body_a: usize,
    /// Index of the second body.
    pub body_b: usize,
    /// Contact normal X component.
    pub normal_x: f32,
    /// Contact normal Y component.
    pub normal_y: f32,
    /// Whether the bodies are currently touching.
    pub is_touching: bool,
}

