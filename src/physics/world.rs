    // ── Contact queries ───────────────────────────────────────────────────────

    /// Returns all contact pairs from the narrow phase.
    ///
    /// # Returns
    /// A vector of `ContactInfo` entries.
    pub fn get_contacts(&self) -> Vec<ContactInfo> {
        let mut contacts = Vec::new();
        for pair in self.narrow_phase.contact_pairs() {
            let id_a = self
                .collider_handles
                .iter()
                .position(|&h| h == pair.collider1);
            let id_b = self
                .collider_handles
                .iter()
                .position(|&h| h == pair.collider2);
            if let (Some(a), Some(b)) = (id_a, id_b) {
                let is_touching = pair.has_any_active_contact();
                let (nx, ny) = pair
                    .manifolds
                    .first()
                    .map(|m| (m.local_n1.x, m.local_n1.y))
                    .unwrap_or((0.0, 0.0));
                contacts.push(ContactInfo {
                    body_a: a,
                    body_b: b,
                    normal_x: nx,
                    normal_y: ny,
                    is_touching,
                });
            }
        }
        contacts
    }

    /// Returns contacts involving a specific body.
    ///
    /// # Parameters
    /// - `body_id` — Body index to filter by.
    ///
    /// # Returns
    /// A vector of `ContactInfo` entries involving the body.
    pub fn get_body_contacts(&self, body_id: usize) -> Vec<ContactInfo> {
        self.get_contacts()
            .into_iter()
            .filter(|c| c.body_a == body_id || c.body_b == body_id)
            .collect()
    }
