    /// Creates a wheel joint (prismatic + rotation) between two bodies.
    ///
    /// Allows translation along the given axis and free rotation.
    ///
    /// # Parameters
    /// - `body_a` — Index of the first body.
    /// - `body_b` — Index of the second body.
    /// - `anchor_x` — Local anchor X.
    /// - `anchor_y` — Local anchor Y.
    /// - `axis_x` — Slide axis X component.
    /// - `axis_y` — Slide axis Y component.
    ///
    /// # Returns
    /// The joint index.
    #[allow(clippy::too_many_arguments)]
    pub fn add_wheel_joint(
        &mut self,
        body_a: usize,
        body_b: usize,
        anchor_x: f32,
        anchor_y: f32,
        axis_x: f32,
        axis_y: f32,
    ) -> usize {
        let ha = match self.body_handles.get(body_a).copied() {
            Some(h) => h,
            None => return 0,
        };
        let hb = match self.body_handles.get(body_b).copied() {
            Some(h) => h,
            None => return 0,
        };
        let axis = Vector::new(axis_x, axis_y);
        let mut joint = PrismaticJointBuilder::new(axis)
            .local_anchor1(Vector::new(anchor_x, anchor_y))
            .local_anchor2(Vector::new(0.0, 0.0))
            .build();
        // Unlock the angular axis to allow rotation (wheel behavior).
        // Prismatic locks Y + AngX; we only want Y locked.
        joint.data.locked_axes = JointAxesMask::LIN_Y;
        let handle = self.impulse_joints.insert(ha, hb, joint, true);
        let jid = self.joint_handles.len();
        self.joint_handles.push(handle);
        self.joint_types.push("wheel");
        jid
    }

    /// Creates a friction joint that resists relative motion between two bodies.
    ///
    /// Uses a fixed joint with velocity motors tuned to the given force/torque limits.
    ///
    /// # Parameters
    /// - `body_a` — Index of the first body.
    /// - `body_b` — Index of the second body.
    /// - `anchor_x` — Local anchor X.
    /// - `anchor_y` — Local anchor Y.
    /// - `max_force` — Maximum linear force.
    /// - `max_torque` — Maximum angular torque.
    ///
    /// # Returns
    /// The joint index.
    #[allow(clippy::too_many_arguments)]
    pub fn add_friction_joint(
        &mut self,
        body_a: usize,
        body_b: usize,
        anchor_x: f32,
        anchor_y: f32,
        max_force: f32,
        max_torque: f32,
    ) -> usize {
        let ha = match self.body_handles.get(body_a).copied() {
            Some(h) => h,
            None => return 0,
        };
        let hb = match self.body_handles.get(body_b).copied() {
            Some(h) => h,
            None => return 0,
        };
        let mut joint = FixedJointBuilder::new()
            .local_anchor1(Vector::new(anchor_x, anchor_y))
            .local_anchor2(Vector::new(0.0, 0.0))
            .build();
        // Configure motors to resist motion (target velocity = 0, damping = max force).
        joint
            .data
            .set_motor(JointAxis::LinX, 0.0, 0.0, 0.0, max_force);
        joint
            .data
            .set_motor(JointAxis::LinY, 0.0, 0.0, 0.0, max_force);
        joint
            .data
            .set_motor(JointAxis::AngX, 0.0, 0.0, 0.0, max_torque);
        let handle = self.impulse_joints.insert(ha, hb, joint, true);
        let jid = self.joint_handles.len();
        self.joint_handles.push(handle);
        self.joint_types.push("friction");
        jid
    }

