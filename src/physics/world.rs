    /// Creates a mouse joint that connects a body to a target point via a spring.
    ///
    /// Internally creates a kinematic anchor body at the target position
    /// and a spring joint between the body and the anchor.
    ///
    /// # Parameters
    /// - `body_id` — Index of the body to attach.
    /// - `target_x` — Initial target X.
    /// - `target_y` — Initial target Y.
    /// - `max_force` — Maximum spring force.
    ///
    /// # Returns
    /// The joint index.
    pub fn add_mouse_joint(
        &mut self,
        body_id: usize,
        target_x: f32,
        target_y: f32,
        max_force: f32,
    ) -> usize {
        // Create a small kinematic body as the mouse anchor.
        let mut anchor = Body::new(target_x, target_y, BodyType::Kinematic);
        anchor.shape = BodyShape::Circle { radius: 0.1 };
        anchor.width = 0.2;
        anchor.height = 0.2;
        let anchor_id = self.add_body(anchor);

        let ha = self.body_handles[body_id];
        let hb = self.body_handles[anchor_id];

        // Use a stiff spring joint.
        let stiffness = max_force;
        let damping = max_force * 0.7;
        let joint = SpringJointBuilder::new(0.0, stiffness, damping)
            .local_anchor1(Vector::new(0.0, 0.0))
            .local_anchor2(Vector::new(0.0, 0.0))
            .build();

        let handle = self.impulse_joints.insert(ha, hb, joint, true);
        let jid = self.joint_handles.len();
        self.joint_handles.push(handle);
        self.joint_types.push("mouse");

        self.mouse_joint_anchors.insert(jid, anchor_id);
        jid
    }

    /// Updates the target position of a mouse joint.
    ///
    /// # Parameters
    /// - `joint_id` — Joint index.
    /// - `x` — New target X.
