//! Internal easing helpers shared by scene transitions.

/// Bounce ease-out helper used by transition curves.
///
/// # Parameters
/// - `t` - `f32` normalised to `[0, 1]`.
///
/// # Returns
/// `f32` eased value.
pub(crate) fn bounce_out(t: f32) -> f32 {
    const N1: f32 = 7.5625;
    const D1: f32 = 2.75;
    if t < 1.0 / D1 {
        N1 * t * t
    } else if t < 2.0 / D1 {
        let t = t - 1.5 / D1;
        N1 * t * t + 0.75
    } else if t < 2.5 / D1 {
        let t = t - 2.25 / D1;
        N1 * t * t + 0.9375
    } else {
        let t = t - 2.625 / D1;
        N1 * t * t + 0.984_375
    }
}
