-- Luna2D Input API Tests

describe("luna.keyboard module exists", function()
    it("luna.keyboard is a table", function()
        expect_type("table", luna.keyboard)
    end)
end)

describe("luna.keyboard functions", function()
    it("isDown is a function", function()
        expect_type("function", luna.keyboard.isDown)
    end)

    it("isDown returns a boolean", function()
        local val = luna.keyboard.isDown("space")
        expect_type("boolean", val)
    end)

    it("isDown returns false for unpressed key", function()
        expect_false(luna.keyboard.isDown("space"))
        expect_false(luna.keyboard.isDown("a"))
        expect_false(luna.keyboard.isDown("escape"))
    end)

    it("isDown accepts multiple keys and returns false when none are pressed", function()
        expect_false(luna.keyboard.isDown("space", "a", "escape"))
    end)

    it("isScancodeDown is a function", function()
        expect_type("function", luna.keyboard.isScancodeDown)
    end)

    it("isScancodeDown returns false for an unpressed scancode", function()
        expect_false(luna.keyboard.isScancodeDown("space"))
    end)

    it("setKeyRepeat and hasKeyRepeat round-trip", function()
        expect_type("function", luna.keyboard.setKeyRepeat)
        expect_type("function", luna.keyboard.hasKeyRepeat)
        expect_false(luna.keyboard.hasKeyRepeat())
        luna.keyboard.setKeyRepeat(true)
        expect_true(luna.keyboard.hasKeyRepeat())
        luna.keyboard.setKeyRepeat(false)
        expect_false(luna.keyboard.hasKeyRepeat())
    end)

    it("setTextInput and hasTextInput round-trip", function()
        expect_type("function", luna.keyboard.setTextInput)
        expect_type("function", luna.keyboard.hasTextInput)
        expect_false(luna.keyboard.hasTextInput())
        luna.keyboard.setTextInput(true)
        expect_true(luna.keyboard.hasTextInput())
        luna.keyboard.setTextInput(false)
        expect_false(luna.keyboard.hasTextInput())
    end)

    it("phase 03 scancode lookup helpers exist", function()
        expect_type("function", luna.keyboard.getScancodeFromKey)
        expect_type("function", luna.keyboard.getKeyFromScancode)
    end)
end)

