describe("keyboard.isModifierActive", function()
    it("returns a boolean for valid modifiers", function()
        expect_type("boolean", luna.keyboard.isModifierActive("shift"))
        expect_type("boolean", luna.keyboard.isModifierActive("ctrl"))
        expect_type("boolean", luna.keyboard.isModifierActive("alt"))
        expect_type("boolean", luna.keyboard.isModifierActive("meta"))
        expect_type("boolean", luna.keyboard.isModifierActive("super"))
    end)
    it("returns false for unknown modifier", function()
        expect_equal(false, luna.keyboard.isModifierActive("capslock"))
    end)
    it("no modifiers held at start", function()
        expect_equal(false, luna.keyboard.isModifierActive("shift"))
        expect_equal(false, luna.keyboard.isModifierActive("ctrl"))
    end)
end)

describe("mouse cursor userdata", function()
    it("getSystemCursor returns a userdata", function()
        local c = luna.mouse.getSystemCursor("arrow")
        expect_type("userdata", c)
    end)
    it("isCursorSupported returns a bool", function()
        expect_type("boolean", luna.mouse.isCursorSupported())
        expect_equal(true, luna.mouse.isCursorSupported())
    end)
    it("getSystemCursor hand cursor returns non-nil", function()
        local c = luna.mouse.getSystemCursor("hand")
        expect_type("userdata", c)
    end)
    it("getSystemCursor crosshair cursor returns userdata", function()
        local c = luna.mouse.getSystemCursor("crosshair")
        expect_type("userdata", c)
    end)
    it("setCursor accepts userdata and updates cursor", function()
        local c = luna.mouse.getSystemCursor("hand")
        luna.mouse.setCursor(c)
        expect_equal("hand", luna.mouse.getCursor())
        luna.mouse.setCursor("arrow")
    end)
    it("setCursor still accepts string for backward compat", function()
        luna.mouse.setCursor("crosshair")
        expect_equal("crosshair", luna.mouse.getCursor())
        luna.mouse.setCursor("arrow")
    end)
end)

