def verb_prefix(name: str) -> Optional[str]:
    """Return the leading verb word if name matches a known prefix."""
    parts = _split_camel(name)
    if not parts:
        return None
    v = parts[0].lower()
    return v


def generate_description(lua_name: str, params: List[Tuple[str, str]], class_name: str) -> str:
    """Generate a one-line description for a method given its name, params, and owning class."""
    parts = _split_camel(lua_name)
    if not parts:
        return f"Calls `{lua_name}`."

    verb = parts[0].lower()
    subject_words = " ".join(p.lower() for p in parts[1:]) if len(parts) > 1 else ""

    # Exact overrides
    if lua_name.lower() in VERB_OVERRIDES:
        return VERB_OVERRIDES[lua_name.lower()][0]

    # Verb-based inference
    if verb in ("get", "fetch", "read"):
        return f"Returns the {subject_words or 'value'}." if subject_words else "Returns the current value."
    if verb in ("set", "write"):
        return f"Sets the {subject_words or 'value'}." if subject_words else "Sets the value."
    if verb in ("is", "has", "can", "was", "should", "will"):
        desc = subject_words or lua_name[len(verb):]
        return f"Returns `true` if {desc or 'the condition is met'}."
    if verb in ("add", "append", "push", "insert", "register", "attach"):
        return f"Adds {subject_words or 'an entry'} to the collection."
    if verb in ("remove", "delete", "erase", "pop", "unregister", "detach"):
        return f"Removes {subject_words or 'the entry'} from the collection."
    if verb in ("new", "create", "make", "spawn", "instantiate"):
        return f"Creates a new {subject_words or class_name} instance."
    if verb in ("count", "size", "length", "num"):
        return f"Returns the number of {subject_words or 'items'}."

    # Full override map lookup (verb_overrides may contain full names)
    if lua_name in VERB_OVERRIDES:
        return VERB_OVERRIDES[lua_name][0]

    # Default
    full_name_words = " ".join(p.lower() for p in parts)
    return f"{full_name_words.capitalize()} on this {class_name}."


def generate_docstring(lua_name: str, params: List[Tuple[str, str]], class_name: str, indent: str) -> str:
    """Return the full /// docstring block as a string (without trailing newline)."""
    desc = generate_description(lua_name, params, class_name)
    lines = [f"{indent}/// {desc}"]

    if params:
        lines.append(f"{indent}///")
        lines.append(f"{indent}/// # Parameters")
        for pname, ptype in params:
            optional = ptype.endswith("?")
            type_display = ptype.rstrip("?")
            if optional:
                lines.append(f"{indent}/// - `{pname}` — `{type_display}` optional.")
            else:
                lines.append(f"{indent}/// - `{pname}` — `{type_display}`.")

    # Add Returns section only for getter-like methods
    verb = _split_camel(lua_name)[0].lower() if _split_camel(lua_name) else ""
    getter_verbs = {"get", "fetch", "read", "is", "has", "can", "was", "count", "size", "num"}
    if verb in getter_verbs or not params:
        # Infer return type description
        if verb in ("is", "has", "can", "was"):
            ret_desc = "`boolean`."
        elif verb in ("count", "size", "num", "length"):
            ret_desc = "`integer`."
        elif verb in ("get", "fetch", "read"):
            subject = _camel_to_words(lua_name)
            ret_desc = f"The current {subject}."
        elif not params:
            ret_desc = "The result."
        else:
            ret_desc = "The result."

        lines.append(f"{indent}///")
        lines.append(f"{indent}/// # Returns")
        lines.append(f"{indent}/// {ret_desc}")

    return "\n".join(lines)


# ── Line analysis ─────────────────────────────────────────────────────────────

BINDING_RE = re.compile(
    r'(?:add_method_mut|add_method|add_function|add_function_mut)\s*\(\s*"([^"]+)"'
)
SET_RE = re.compile(r'\.set\s*\(\s*"([^"]+)"')
STRUCT_RE = re.compile(r'impl(?:\s+LuaUserData\s+for\s+Lua)?(\w+)')


def line_has_doc_above(lines: List[str], idx: int) -> bool:
    """Return True if there is a /// doc comment immediately above line idx."""
    j = idx - 1
    while j >= 0:
        s = lines[j].strip()
        if s.startswith("///"):
            return True
        if s == "" or s.startswith("#[") or s.startswith("//!"):
            j -= 1
            continue
        if re.match(r'^let\s+\w+\s*=\s*\w+(?:\.\w+)*\s*\(\s*\)\s*;$', s):
            j -= 1
            continue
        if s.startswith("//") and not s.startswith("///"):
            j -= 1
            continue
        break
    return False


def current_struct_name(lines: List[str], idx: int) -> str:
    """Walk backwards from idx to find the most recent 'impl LuaUserData for LuaXxx'."""
    for j in range(idx - 1, -1, -1):
        m = re.search(r'impl\s+LuaUserData\s+for\s+Lua(\w+)', lines[j])
        if m:
            return m.group(1)
        m2 = re.search(r'struct\s+Lua(\w+)', lines[j])
        if m2:
            return m2.group(1)
    return "Object"


# ── File processor ────────────────────────────────────────────────────────────

def process_file(path: pathlib.Path, dry_run: bool = False) -> int:
    """Process one Rust source file; return number of docstrings inserted."""
    text = path.read_text(encoding='utf-8')
    lines = text.splitlines()
    insertions: List[Tuple[int, str]] = []  # (line_idx, docstring_text)

    for i, line in enumerate(lines):
        m = BINDING_RE.search(line) or SET_RE.search(line)
        if not m:
            continue
        lua_name = m.group(1)
        # Skip internal infrastructure names
        if lua_name in ('__index', '__newindex', '__tostring', '__len',
                        '__eq', '__lt', '__le', '__call', '__gc',
                        'type', 'is_a', 'inherits', 'super'):
            continue
        if line_has_doc_above(lines, i):
            continue

        params = parse_closure_params(lines, i)
        class_name = current_struct_name(lines, i)
        indent = re.match(r'^(\s*)', line).group(1)
        doc = generate_docstring(lua_name, params, class_name, indent)
        insertions.append((i, doc))

    if not insertions:
        return 0

    if dry_run:
        return len(insertions)

    # Insert in reverse order so line indices remain valid
    for idx, doc in reversed(insertions):
        lines.insert(idx, doc)

    path.write_text("\n".join(lines) + "\n", encoding='utf-8')
    return len(insertions)


# ── CLI ───────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument('--dry-run', action='store_true', help='Print counts only')
    parser.add_argument('--file', help='Process single file name (relative to src/lua_api/)')
    args = parser.parse_args()

    if args.file:
        files = [LUA_API_DIR / args.file]
    else:
        files = sorted(LUA_API_DIR.glob('*.rs'))

    total = 0
    for f in files:
        n = process_file(f, dry_run=args.dry_run)
        if n:
            verb = "would insert" if args.dry_run else "inserted"
            print(f"  {f.name}: {verb} {n} docstrings")
            total += n

    print(f"\nTotal: {total} docstrings {'would be ' if args.dry_run else ''}inserted.")
    return 0


if __name__ == '__main__':
    sys.exit(main())
