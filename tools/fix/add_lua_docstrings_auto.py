#!/usr/bin/env python3
"""
add_lua_docstrings_auto.py — Automatically inject /// docstrings above every
undocumented add_method / add_function / .set() call in src/lua_api/*.rs.

Docstrings are generated by:
 - Splitting the camelCase method name into words
 - Inferring intent from common prefixes (get/set/is/has/new/add/remove/…)
 - Parsing the Rust closure parameter list for param names and types
 - Using the context struct name to provide class-aware descriptions

Usage:
    python tools/add_lua_docstrings_auto.py [--dry-run] [--file FILE]

    --dry-run   Print how many insertions would be made; do not write files.
    --file FILE Process only this one file (relative to src/lua_api/).
"""

import re
import sys
import pathlib
import argparse
from typing import List, Tuple, Optional

WORKSPACE = pathlib.Path(__file__).resolve().parent.parent.parent.parent
LUA_API_DIR = WORKSPACE / "src" / "lua_api"

# ── camelCase splitter ────────────────────────────────────────────────────────

def _split_camel(name: str) -> List[str]:
    """'setMaxSpeed' → ['set', 'Max', 'Speed']"""
    parts = re.sub(r'([A-Z])', r' \1', name).split()
    return parts


def _camel_to_words(name: str) -> str:
    """'setMaxSpeed' → 'max speed'"""
    parts = _split_camel(name)
    if not parts:
        return name.lower()
    # skip first word (verb) to get the subject
    return " ".join(p.lower() for p in parts[1:]) if len(parts) > 1 else parts[0].lower()


# ── Rust type → Lua type ──────────────────────────────────────────────────────

RUST_TO_LUA = {
    "f32": "number", "f64": "number",
    "i8": "integer", "i16": "integer", "i32": "integer", "i64": "integer",
    "u8": "integer", "u16": "integer", "u32": "integer", "u64": "integer",
    "usize": "integer", "isize": "integer",
    "String": "string", "LuaString": "string",
    "bool": "boolean",
    "LuaTable": "table",
    "LuaValue": "any",
    "LuaAnyUserData": "userdata",
    "LuaFunction": "function",
    "()": "nil",
}

def rust_to_lua_type(t: str) -> str:
    t = t.strip()
    m = re.match(r'Option<(.+)>', t)
    if m:
        return rust_to_lua_type(m.group(1)) + "?"
    m = re.match(r'Vec<(.+)>', t)
    if m:
        return "table"
    return RUST_TO_LUA.get(t, t)


# ── Closure parameter extraction ─────────────────────────────────────────────

def parse_closure_params(lines: List[str], decl_idx: int) -> List[Tuple[str, str]]:
    """
    Parse the closure parameter list from lines starting at decl_idx.
    Returns a list of (name, lua_type) pairs. Empty list if no params or unparseable.
    """
    # Collect up to 8 lines of context for multi-line closures
    window = " ".join(l.strip() for l in lines[decl_idx: decl_idx + 8])

    # Pattern 1: |_, this, (a, b, c): (T1, T2, T3)| or |_, this, (a,): (T1,)|
    m = re.search(r'\|[^|]*?,\s*(?:\w+,\s*)?\(([^)]+)\):\s*\(([^)]+)\)', window)
    if m:
        names_raw = m.group(1).split(',')
        types_raw = m.group(2).split(',')
        result = []
        for n, t in zip(names_raw, types_raw):
            name = re.sub(r'^_+', '', n.strip())
            lua_t = rust_to_lua_type(t.strip())
            if name and lua_t:
                result.append((name, lua_t))
        return result

    # Pattern 2: single param with tuple wrap: (a,): (T,)
    m = re.search(r'\|[^|]*?,\s*(?:\w+,\s*)?\((\w+)\s*,\s*\):\s*\(([^)]+),\s*\)', window)
    if m:
        name = re.sub(r'^_+', '', m.group(1))
        lua_t = rust_to_lua_type(m.group(2).strip())
        return [(name, lua_t)]

    # Pattern 3: scalar param: |_, this, name: Type|
    m = re.search(r'\|[^|]*?,\s*(?:\w+,\s*)?(\w+):\s*([A-Za-z][A-Za-z0-9_<>, &]+?)\|', window)
    if m:
        name = re.sub(r'^_+', '', m.group(1))
        lua_t = rust_to_lua_type(m.group(2).strip())
        if name not in ('lua', '_lua', 'this', '_'):
            return [(name, lua_t)]

    # Pattern 4: truly no params: |_, ()| or |_, this, ()|
    m = re.search(r'\|[^|]*?,\s*(?:\w+,\s*)?\(\s*\)\|', window)
    if m:
        return []

    # Fallback: assume no params
    return []


