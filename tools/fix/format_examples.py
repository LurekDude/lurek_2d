#!/usr/bin/env python3
"""
tools/fix/format_examples.py
Lurek2D — Normalise formatting in all content/examples/*.lua files.

Pass 1 — Remove AUTO-EXPANDED banners and === separators.
Pass 2 — Fix section headers generated by expand_examples.py:
    -- -- ClassName ----  →  -- ─── ClassName ────────────────
Pass 3 — Deduplicate adjacent comments:
    If two consecutive non-blank lines are:
        -- Some description
        call()  -- Some description
    then drop the first line (the standalone comment).
Pass 4 — Trim trailing whitespace.
Pass 5 — Collapse 3+ consecutive blank lines to 2.
Pass 6 — Fix variable naming: local type = → local type_val =
Pass 7 — Fix "(assumes 'x' is an existing X instance)" pattern into nicer comment.

Usage:
    python tools/fix/format_examples.py           # apply to all
    python tools/fix/format_examples.py --dry-run # preview only
"""
from __future__ import annotations
import argparse
import re
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
EXAMPLES_DIR = ROOT / "content" / "examples"

_SECTION_RE = re.compile(r'^(-- )-- ([A-Za-z0-9_.]+) -+\s*$')


_AUTO_EXPANDED_RE = re.compile(r'^-- (?:AUTO-EXPANDED SECTION.*|={10,}.*)$')
_ASSUMES_RE = re.compile(r"^-- \(assumes '(\w+)' is an existing (\w+) instance\)\s*$")


def strip_banners(lines: list[str]) -> list[str]:
    """Remove AUTO-EXPANDED banner lines and === separators."""
    out = []
    for line in lines:
        if _AUTO_EXPANDED_RE.match(line.strip()):
            continue
        # Rewrite "(assumes 'x' is an existing X instance)" → nicer comment
        m = _ASSUMES_RE.match(line)
        if m:
            cls = m.group(2)
            var = m.group(1)
            line = f"-- {cls} instance methods (variable: {var})"
        out.append(line)
    return out


def fix_section_headers(lines: list[str]) -> list[str]:
    """Replace  -- -- Name ---  with  -- ─── Name ──────────────────────"""
    out = []
    for line in lines:
        m = _SECTION_RE.match(line)
        if m:
            prefix = m.group(1)
            title = m.group(2)
            bar_len = max(0, 74 - len(title))
            bar = "\u2500" * bar_len
            line = f"{prefix}\u2500\u2500\u2500 {title} {bar}"
        out.append(line)
    return out


_INLINE_COMMENT_RE = re.compile(r'--\s*(.+)$')


def deduplicate_comments(lines: list[str]) -> list[str]:
    """
    If a standalone comment line is immediately followed by a code line
    that has the exact same text as an inline comment, drop the standalone
    comment (it's redundant).
    """
    out: list[str] = []
    i = 0
    while i < len(lines):
        line = lines[i]
        stripped = line.strip()
        # Is this a standalone comment (not a section header)?
        if stripped.startswith("-- ") and not stripped.startswith("-- ─") and not stripped.startswith("-- ="):
            comment_text = stripped[3:].strip()
            # peek at next non-blank line
            j = i + 1
            while j < len(lines) and lines[j].strip() == "":
                j += 1
            if j < len(lines):
                next_line = lines[j].rstrip()
                inline_m = _INLINE_COMMENT_RE.search(next_line)
                if inline_m and inline_m.group(1).strip() == comment_text:
                    # duplicate — skip the standalone comment
                    i += 1
                    continue
        out.append(line)
        i += 1
    return out


_RESERVED = re.compile(r'\blocal (type|end|do|if|in|or|and|not|nil)\b')


def fix_reserved_varnames(lines: list[str]) -> list[str]:
    """Rename local variables that shadow Lua keywords or builtins."""
    renames = {
        "local type ": "local type_val ",
        "local end ":  "local end_val ",
    }
    out = []
    for line in lines:
        for old, new in renames.items():
            line = line.replace(old, new)
        out.append(line)
    return out


def collapse_blank_lines(lines: list[str]) -> list[str]:
    """Collapse runs of 3+ blank lines down to 2."""
    out: list[str] = []
    blank_run = 0
    for line in lines:
        if line.strip() == "":
            blank_run += 1
            if blank_run <= 2:
                out.append(line)
        else:
            blank_run = 0
            out.append(line)
    return out


def trim_trailing(lines: list[str]) -> list[str]:
    return [ln.rstrip() for ln in lines]


def process_file(path: Path, dry_run: bool = False) -> bool:
    """Return True if the file was changed."""
    original = path.read_text(encoding="utf-8")
    lines = original.splitlines()

    lines = strip_banners(lines)
    lines = fix_section_headers(lines)
    lines = deduplicate_comments(lines)
    lines = fix_reserved_varnames(lines)
    lines = collapse_blank_lines(lines)
    lines = trim_trailing(lines)

    result = "\n".join(lines)
    if result.rstrip() != original.rstrip():
        if not dry_run:
            path.write_text(result + "\n", encoding="utf-8")
        return True
    return False


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--dry-run", action="store_true")
    args = parser.parse_args()

    changed = 0
    for p in sorted(EXAMPLES_DIR.glob("*.lua")):
        if process_file(p, args.dry_run):
            changed += 1
            print(f"[FIX] {p.name}")
        else:
            print(f"[OK ] {p.name}")

    print(f"\n{'Would change' if args.dry_run else 'Changed'}: {changed} files")


if __name__ == "__main__":
    main()
