#!/usr/bin/env python3
"""
tools/fix/fix_type_stub_vars.py
Luna2D — Fix duplicate local var names in example files.

Every class section generated by expand_examples.py ends with:
    local class_name = obj:type()
    local is_XYZ_type = obj:typeOf("XYZ")

When multiple classes appear in the same file these vars shadow each other.
This script renames each to use the object variable as prefix:
    local bus_type      = bus:type()
    local is_bus_type   = bus:typeOf("Bus")
    local agent_type    = agent:type()
    local is_agent_type = agent:typeOf("Agent")

Usage:
    python tools/fix/fix_type_stub_vars.py           # all files
    python tools/fix/fix_type_stub_vars.py --dry-run  # show diffs only
"""
from __future__ import annotations

import argparse
import re
from pathlib import Path

ROOT       = Path(__file__).resolve().parents[2]
EXAMPLES   = ROOT / "examples"

# Matches:  local class_name = VAR:type()  -- optional comment
_TYPE_RE = re.compile(
    r'^(\s*)local\s+class_name\s*=\s*(\w+):type\(\)(.*)$'
)

# Matches: local is_XXX_type = VAR:typeOf("YYY")  -- optional comment
_TYPEOF_RE = re.compile(
    r'^(\s*)local\s+is_\w+\s*=\s*(\w+):typeOf\(([^)]*)\)(.*)$'
)


def fix_file(path: Path, dry_run: bool = False) -> bool:
    original = path.read_text(encoding="utf-8")
    out_lines = []
    changed   = False

    for line in original.splitlines():
        new_line = line

        m_type = _TYPE_RE.match(line)
        if m_type:
            indent, var, rest = m_type.group(1), m_type.group(2), m_type.group(3)
            new_line = f"{indent}local {var}_type = {var}:type(){rest}"
            changed  = changed or (new_line != line)

        else:
            m_typeof = _TYPEOF_RE.match(line)
            if m_typeof:
                indent = m_typeof.group(1)
                var    = m_typeof.group(2)
                arg    = m_typeof.group(3)
                rest   = m_typeof.group(4)
                new_line = f"{indent}local {var}_is_type = {var}:typeOf({arg}){rest}"
                changed  = changed or (new_line != line)

        out_lines.append(new_line)

    if changed:
        if not dry_run:
            path.write_text("\n".join(out_lines) + "\n", encoding="utf-8")
        return True
    return False


def main() -> None:
    parser = argparse.ArgumentParser(description="Fix duplicate type-stub var names")
    parser.add_argument("--dry-run", action="store_true")
    args = parser.parse_args()

    fixed = 0
    for p in sorted(EXAMPLES.glob("*.lua")):
        if fix_file(p, args.dry_run):
            fixed += 1
            print(f"[FIX] {p.name}")

    print(f"\n{'Would fix' if args.dry_run else 'Fixed'} {fixed} file(s)")


if __name__ == "__main__":
    main()
