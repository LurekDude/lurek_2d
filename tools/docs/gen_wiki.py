# ── Utility ───────────────────────────────────────────────────────────────────

def _read(path: Path) -> str:
    """Read a file; return empty string if missing."""
    if not path.exists():
        return ""
    return path.read_text(encoding="utf-8")


def _section(text: str, name: str) -> str:
    """Extract content under '## name' heading up to the next '##' or EOF."""
    pat = rf"(?m)^## {re.escape(name)}\n(.*?)(?=^##|\Z)"
    m = re.search(pat, text, re.DOTALL)
    return m.group(1).strip() if m else ""


def _prop(text: str, key: str) -> str:
    """Extract value from '| **key** | value |' metadata table row."""
    pat = rf"\*\*{re.escape(key)}\*\*\s*\|\s*(.+?)\s*(?:\||\n)"
    m = re.search(pat, text)
    return m.group(1).strip() if m else ""


def _strip_source_refs(text: str) -> str:
    """Remove references to source file paths (src/, tests/, content/library/ paths)."""
    # Replace `src/module/` references with descriptive text
    text = re.sub(r'`src/([a-z_]+)/`', r'the \1 module', text)
    text = re.sub(r'`src/([a-z_]+)/([a-z_]+\.rs)`', r'the \1 module', text)
    text = re.sub(r'`tests/[^`]+`', 'the test suite', text)
    # Remove "Source: `src/...`" lines
    text = re.sub(r'\s*·\s*Source:\s*`[^`]+`', '', text)
    return text


def read_spec(module: str) -> dict:
    """Read a spec file and return parsed metadata dict."""
    text = _read(SPECS / f"{module}.md")
    if not text:
        return {}
    return {
        "raw":     text,
        "tier":    _prop(text, "Tier"),
        "status":  _prop(text, "Status"),
        "api":     _prop(text, "Lua API"),
        "source":  _prop(text, "Source"),
        "summary": _section(text, "Summary"),
        "arch":    _section(text, "Architecture"),
        "lua_api": _section(text, "Lua API"),
        "types":   _section(text, "Types"),
        "cross":   _section(text, "Cross-Module References"),
    }


def _spec_summary(module: str, n_paragraphs: int = 3) -> str:
    """Return first n_paragraphs of spec Summary."""
    spec = read_spec(module)
    text = spec.get("summary", "")
    text = re.sub(r"```.*?```", "", text, flags=re.DOTALL)
    paras = [p.strip() for p in text.split("\n\n") if p.strip()]
    return "\n\n".join(paras[:n_paragraphs])


def _spec_lua_api(module: str) -> str:
    """Return the Lua API section from the spec, cleaned of source refs."""
    spec = read_spec(module)
    text = spec.get("lua_api", "")
    return _strip_source_refs(text)


def read_example(module: str, max_lines: int = 120) -> str:
    """Read an example file, stripping the boilerplate header comment."""
    text = _read(EXAMPLES / f"{module}.lua")
    if not text:
        return ""
    lines = text.split("\n")
    cut = 0
    for i, ln in enumerate(lines[:8]):
        if re.match(r"^-- (content/examples/|Lurek2D\b)", ln):
            cut = i + 1
    return "\n".join(lines[cut:max_lines]).strip()


def write_page(filename: str, content: str, dry_run: bool = False,
               is_manual: bool = False, force: bool = False) -> str:
    """Write a wiki page. Returns status string."""
    path = WIKI / filename
    if dry_run:
        print(f"\n{'='*60}\n{filename} ({'MANUAL' if is_manual else 'SCRIPTED'})\n{'='*60}")
        print(content[:800])
        print("  [... truncated ...]")
        return "dry-run"
    if is_manual and path.exists() and not force:
        print(f"  skip   docs/wiki/{filename} (manual page exists)")
        return "skipped"
    path.write_text(content, encoding="utf-8")
    tag = "wrote " if not is_manual else "scaffolded"
    print(f"  {tag} docs/wiki/{filename}")
    return "wrote"


