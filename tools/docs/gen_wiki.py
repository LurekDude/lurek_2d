# CLI entry point
# ═══════════════════════════════════════════════════════════════════════════════

def main() -> int:
    parser = argparse.ArgumentParser(
        description="Regenerate all Lurek2D wiki pages from source.",
    )
    parser.add_argument(
        "--page", metavar="SLUG",
        help="Regenerate a single page by slug (see --list)",
    )
    parser.add_argument(
        "--list", action="store_true",
        help="List all available page slugs and exit",
    )
    parser.add_argument(
        "--dry-run", action="store_true",
        help="Print generated content without writing files",
    )
    parser.add_argument(
        "--force", action="store_true",
        help="Overwrite manual pages (normally skipped if they exist)",
    )
    args = parser.parse_args()

    if args.list:
        scripted = 0
        manual = 0
        for slug, (filename, _, is_manual) in sorted(PAGES.items()):
            tag = "MANUAL" if is_manual else "SCRIPTED"
            print(f"  {slug:30s}  →  docs/wiki/{filename:35s}  [{tag}]")
            if is_manual:
                manual += 1
            else:
                scripted += 1
        print(f"\n  Total: {scripted + manual} pages ({scripted} scripted, {manual} manual)")
        return 0

    WIKI.mkdir(parents=True, exist_ok=True)

    if args.page:
        if args.page not in PAGES:
            print(f"ERROR: unknown page slug '{args.page}'. Run --list to see options.",
                  file=sys.stderr)
            return 1
        targets = {args.page: PAGES[args.page]}
    else:
        targets = PAGES

    errors = 0
    wrote = 0
    skipped = 0
    for slug, (filename, gen_fn, is_manual) in targets.items():
        try:
            content = gen_fn()
            result = write_page(filename, content, dry_run=args.dry_run,
                                is_manual=is_manual, force=args.force)
            if result == "wrote":
                wrote += 1
            elif result == "skipped":
                skipped += 1
        except Exception as exc:  # noqa: BLE001
            print(f"  ERROR  {filename}: {exc}", file=sys.stderr)
            errors += 1

    if not args.dry_run:
        total = len(targets)
        status = "✓" if errors == 0 else "!"
        print(f"\n{status} {wrote} wrote, {skipped} skipped (manual), {errors} errors — {total} pages total")
    return 0 if errors == 0 else 1


if __name__ == "__main__":
    sys.exit(main())
