def gen_cag_system() -> str:
    """Generate CAG-System.md from .github/ content."""
    agents_dir = GITHUB / "agents"
    skills_dir = GITHUB / "skills"

    agent_rows = []
    if agents_dir.exists():
        for f in sorted(agents_dir.glob("*.agent.md")):
            text = _read(f)
            fm_match = re.search(
                r'^description:\s*["\']?(.*?)["\']?\s*$',
                text, re.MULTILINE,
            )
            if fm_match:
                raw = fm_match.group(1).strip().strip('"\'')
                raw = re.sub(r'^\*\*[^*]+\*\*\s*—\s*', '', raw)
                mission = raw[:150]
            else:
                mm = re.search(r"## Mission\s*\n(.+?)(?:\n|$)", text)
                mission = mm.group(1).strip()[:150] if mm else "—"
            name_match = re.search(r'^name:\s*(.+)$', text, re.MULTILINE)
            display = name_match.group(1).strip() if name_match else f.stem.replace(".agent", "")
            agent_rows.append(f"| **{display}** | {mission} |")

    skill_rows = []
    if skills_dir.exists():
        for subdir in sorted(skills_dir.iterdir()):
            if not subdir.is_dir():
                continue
            skill_md = _read(subdir / "SKILL.md")
            desc = ""
            for ln in skill_md.split("\n")[:20]:
                ln = ln.strip()
                if ln and not ln.startswith("#") and not ln.startswith("<!--"):
                    desc = ln[:140]
                    break
            skill_rows.append(f"| `{subdir.name}` | {desc} |")

    agent_table = "\n".join(agent_rows) if agent_rows else "_No agents found._"
    skill_table = "\n".join(skill_rows) if skill_rows else "_No skills found._"

    return GEN_BANNER + dedent(f"""\
    # CAG System — Copilot Agent Customization

    The CAG (Copilot Agent Customization) layer configures GitHub Copilot for
    AI-first Lurek2D development. It provides specialist agents, domain skills,
    and reusable prompts.

    ---

    ## Overview

    The CAG layer follows a strict load order:

    ```
    System Prompt (copilot-instructions.md)
      └── Module AGENT.md files (overview per module)
          └── Spec files (full technical detail)
              └── Skills (loaded on demand)
                  └── Prompts / Agents (task-specific)
    ```

    ---

    ## Agents

    Specialist agents, each with a defined mission:

    | Agent | Mission |
    |-------|---------|
    {agent_table}

    ---

    ## Skills

    Domain-specific knowledge bundles loaded when a matching task is detected:

    | Skill | Description |
    |-------|-------------|
    {skill_table}

    ---

    ## Validation

    After editing any CAG file, validate:

    ```bash
    python tools/validate/cag_validate.py              # Full validation
    python tools/validate/cag_validate.py --file <path>  # Single file
    ```

    ---

    See also: [[CAG-Workflow]] · [[Contributing-CAG]] · [[VS-Code-Extension]] · [[Tools]]
    """)


def gen_cag_workflow() -> str:
    """MANUAL page — how to use CAG for development."""
    return MANUAL_BANNER + dedent("""\
    # CAG Workflow

    How the Copilot Agent Customization (CAG) system works in practice.

    ---

    ## What Happens When You Ask Copilot

    1. **System prompt** loads automatically — engine constraints, module rules, API conventions
    2. **Module AGENT.md** files provide short overviews of relevant modules
    3. **Spec files** are loaded for deep technical detail when needed
    4. **Skills** are loaded on demand based on the task domain
    5. **Agent mode** activates if you're in a specific agent (e.g., Manager, Developer, Tester)

    ---

    ## Agent Workflow

    For multi-step tasks, the Manager agent:

    1. Confirms the current Git branch
    2. Creates a work session folder with standard subfolders
    3. Decomposes the task into agent handoffs
    4. Routes each subtask to the most specific specialist
    5. Runs quality gates between phases
    6. Commits after each accepted phase

    ---

    ## Session Folders

    Every session creates:

    ```
    work/<session-name>/
    ├── scripts/         -- session-scoped scripts
    ├── handovers/       -- inter-agent context
    ├── reports/         -- analysis output
    ├── data/            -- working data
    ├── content/demos/           -- session demos
    ├── other/           -- uncategorised
    ├── temp/            -- temporary files
    └── logs/
        └── agent_log.jsonl
    ```

    ---

    ## Skills vs Agents

    | Concept | What it is | When it loads |
    |---------|------------|---------------|
    | **Skill** | Domain knowledge bundle | Automatically, when task matches skill description |
    | **Agent** | Specialist persona with defined scope | When explicitly invoked or routed to |

    ---

    ## Quality Gates

    Every phase ends with:

    ```bash
    cargo test && cargo clippy -- -D warnings
    ```

    Commit only after the gate passes.

    ---

    See also: [[CAG-System]] · [[Contributing-CAG]] · [[Architecture]]
    """)


